export function getStandardsSubjectForRecommendation(quizSubjects, state) {
  // All Subjects and state Map this Map will be used as we add support for other subjects

  // const stateSubjectsMap = {
  //   CommonCore: ['Mathematics', 'English Language Arts'],
  //   TX: ['Mathematics', 'English Language Arts and Reading', 'Science'],
  //   VA: ['Mathematics (2016)', 'English (2017)', 'Science'],
  //   NY: ['Common Core Mathematics (2010)', 'Science (2016)'],
  //   CA: ['Mathematics', 'Science'],
  //   GA: ['Mathematics - Georgia Standards of Excellence', 'English Language Arts - Georgia Standards of Excellence', 'Science - Georgia Standards of Excellence'],
  // };

  const stateSubjectsMap = {
    CommonCore: ['Mathematics'],
    TX: ['Mathematics'],
    VA: ['Mathematics (2016)'],
    NY: ['Next Generation Mathematics Learning Standards (K-8) (2017)'],
    CA: ['Mathematics'],
    GA: ['Mathematics - Georgia Standards of Excellence'],
  };

  let subjectForQuery = '';

  quizSubjects.forEach((subject) => {
    if (stateSubjectsMap[state]) {
      stateSubjectsMap[state].forEach((standardSubject) => {
        if (standardSubject.toLowerCase().includes(subject.toLowerCase())) {
          subjectForQuery = standardSubject;
        }
      });
    } else {
      stateSubjectsMap.CommonCore?.forEach((standardSubject) => {
        if (standardSubject.toLowerCase().includes(subject.toLowerCase())) {
          subjectForQuery = standardSubject;
        }
      });
    }
  });
  return subjectForQuery;
}

